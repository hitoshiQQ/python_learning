def find_k_by_S(S):
    K = 0
    while True:
        # общее количество костяшек в наборе K-домино
        N = (K + 1) * (K + 2) // 2
        # количество гарантированно выбракованных (двойных): K+1
        guaranteed = K + 1
        # максимум дополнительных выбракованных: floor((K+1)/2)
        extra_max = (K + 1) // 2
        # минимально и максимально доставленных костяшек
        D_max = N - guaranteed
        D_min = N - (guaranteed + extra_max)

        if D_min <= S <= D_max:
            return K

        # если даже максимальное доставленное меньше S, увеличиваем K
        if D_max < S:
            K += 1
            continue

        # если минимальное доставленное больше S,
        # дальше K только увеличивает D_min,
        # значит решения нет (можно вернуть -1 или выбросить)
        return -1


if __name__ == "__main__":
    S = int(input())
    print(find_k_by_S(S))
