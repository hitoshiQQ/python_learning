# n ‚Äî –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø–∞—Ä —Ü–∏—Ñ—Ä,
# —Å–∏–º–º–µ—Ç—Ä–∏—á–Ω–æ —Ä–∞—Å–ø–æ–ª–æ–∂–µ–Ω–Ω—ã—Ö –æ—Ç–Ω–æ—Å–∏—Ç–µ–ª—å–Ω–æ —Å–µ—Ä–µ–¥–∏–Ω—ã –∑–∞–ø–∏—Å–∏,
# –≤ –∫–æ—Ç–æ—Ä—ã—Ö —Ü–∏—Ñ—Ä—ã —Å–æ–≤–ø–∞–¥–∞—é—Ç.
# –ü–∞—Ä–æ–π —Å—á–∏—Ç–∞–µ—Ç—Å—è (i,j), –≥–¥–µ i ‚Äî –ø–æ–∑–∏—Ü–∏—è —Ü–∏—Ñ—Ä—ã —Å –Ω–∞—á–∞–ª–∞ (0-–∏–Ω–¥–µ–∫—Å),
# j ‚Äî —Å–∏–º–º–µ—Ç—Ä–∏—á–Ω–∞—è –ø–æ–∑–∏—Ü–∏—è —Å –∫–æ–Ω—Ü–∞: ùëó=ùêø-1-i
# –ï—Å–ª–∏ i=j (—Ü–µ–Ω—Ç—Ä–∞–ª—å–Ω–∞—è —Ü–∏—Ñ—Ä–∞ –ø—Ä–∏ –Ω–µ—á—ë—Ç–Ω–æ–π –¥–ª–∏–Ω–µ),
# —Ç–æ —ç—Ç–æ —Ç–æ–∂–µ –ø–∞—Ä–∞ –∏ —Å—Ä–∞–≤–Ω–µ–Ω–∏–µ –≤—Å–µ–≥–¥–∞ –∏—Å—Ç–∏–Ω–Ω–æ (—Ü–∏—Ñ—Ä–∞ —Ä–∞–≤–Ω–∞ —Å–∞–º–æ–π —Å–µ–±–µ),
# –ø–æ—ç—Ç–æ–º—É –¥–∞—ë—Ç +1.
# –í–≤–æ–¥ —á–∏—Å–ª–∞
n = input().strip()

# –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –≤ —Å—Ç—Ä–æ–∫—É
s = n
L = len(s)
counter = 0

# –ü—Ä–æ–≤–µ—Ä—è–µ–º —Å–∏–º–º–µ—Ç—Ä–∏—á–Ω—ã–µ –ø–∞—Ä—ã
for i in range((L + 1) // 2):
    if s[i] == s[L - 1 - i]:
        counter += 1

# –í—ã–≤–æ–¥–∏–º —Å—Ç–µ–ø–µ–Ω—å —Å–∏–º–º–µ—Ç—Ä–∏–∏
print(counter)
