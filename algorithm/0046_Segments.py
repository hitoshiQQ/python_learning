# –ß—Ç–æ–±—ã –ø—Ä–∏ —Ñ–∏–∫—Å–∏—Ä–æ–≤–∞–Ω–Ω–æ–º —á–∏—Å–ª–µ —á–∞—Å—Ç–µ–π ùëõ –º–∏–Ω–∏–º–∏–∑–∏—Ä–æ–≤–∞—Ç—å —Å—É–º–º–∞—Ä–Ω—É—é –¥–ª–∏–Ω—É
# (–∏ —Ç–µ–º —Å–∞–º—ã–º –ø–æ–∑–≤–æ–ª–∏—Ç—å —É–º–µ—Å—Ç–∏—Ç—å –∫–∞–∫ –º–æ–∂–Ω–æ –±–æ–ª—å—à–∏–π
# ùëõ –ø—Ä–∏ –¥–∞–Ω–Ω–æ–º ùëô), –±–µ—Ä—ë–º –º–∏–Ω–∏–º–∞–ª—å–Ω–æ –≤–æ–∑–º–æ–∂–Ω—ã–µ —Ü–µ–ª—ã–µ ùë•i.
# –ú–∏–Ω–∏–º–∞–ª—å–Ω—ã–µ —Å—Ç–∞—Ä—Ç–æ–≤—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è ‚Äî ùë•1 = 1, ùë•2=1,
# –∞ –¥–∞–ª–µ–µ –ø–æ –Ω–µ—Å—Ç—Ä–æ–≥–æ–º—É —Ä–µ–∫—É—Ä—Ä–µ–Ω—Ç—É xk = xk -1 + xk -2
# –ü–æ–ª—É—á–∞–µ–º –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç—å –§–∏–±–æ–Ω–∞—á—á–∏ 1,1,2,3,5,‚Ä¶.
# –°—É–º–º–∞ –ø–µ—Ä–≤—ã—Ö ùëõ —Ç–∞–∫–∏—Ö —á–∏—Å–µ–ª ‚Äî –º–∏–Ω–∏–º–∞–ª—å–Ω–∞—è –≤–æ–∑–º–æ–∂–Ω–∞—è —Å—É–º–º–∞—Ä–Ω–∞—è –¥–ª–∏–Ω–∞
# –ø—Ä–∏ n —á–∞—Å—Ç—è—Ö.

# —á–∏—Ç–∞–µ–º l
length = int(input().strip())

# –º–∏–Ω–∏–º–∞–ª—å–Ω—ã–µ –ø–µ—Ä–≤—ã–µ –¥–≤–∞ –æ—Ç—Ä–µ–∑–∫–∞ = 1,1
if length < 2:
    print(0)
else:
    # f1 = F_{k-1}, f2 = F_k (–Ω–∞—á–∏–Ω–∞–µ–º —Å f1=1,f2=1 -> –¥–≤–∞ —ç–ª–µ–º–µ–Ω—Ç–∞, —Å—É–º–º–∞=2)
    f1, f2 = 1, 1
    total = 2
    count = 2
    if length < total:
        # –Ω–∞ –≤—Å—è–∫–∏–π —Å–ª—É—á–∞–π (–¥–ª—è l==1), –Ω–æ –ø–æ —É—Å–ª–æ–≤–∏—é l>=4
        # –µ—Å–ª–∏ l==1 -> 1 —á–∞—Å—Ç—å, l==2 -> 2 —á–∞—Å—Ç–∏
        if length == 1:
            print(1)
        else:
            print(1 if length == 1 else 2)
    else:
        while True:
            # —Å–ª–µ–¥—É—é—â–∏–π —Ñ–∏–±
            f3 = f1 + f2
            if total + f3 > length:
                break
            total += f3
            count += 1
            f1, f2 = f2, f3
        print(count)
